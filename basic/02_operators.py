### Operadores Comparativos ###

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4) # Es igual
print(3 != 4) # No es igual 
print(3 > 4 == 4)

# Ordenación alfabética por ASCII
print("aaa" < "aba")

#Cuenta caracteres (len es para la longitud)
